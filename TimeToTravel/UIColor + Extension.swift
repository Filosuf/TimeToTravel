import UIKit
import Foundation

extension UIColor {
    struct WbTheme {
        static var magenta: UIColor  { return UIColor(red: 203/255, green: 17/255, blue: 171/255, alpha: 1) }
        static var purplish: UIColor { return UIColor(red: 153/255, green: 0/255, blue: 153/255, alpha: 1) }
        static var darkViolet: UIColor { return UIColor(red: 72/255, green: 17/255, blue: 115/255, alpha: 1) }
    }
}
